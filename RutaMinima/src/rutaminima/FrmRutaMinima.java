    public FrmRutaMinima() {
        initComponents();
 
        String nombreArchivo = System.getProperty("user.dir") + "/src/datos/Distancias.txt";
        g.desdeArchivo(nombreArchivo);
        
        g.mostrarNodos(tblNodos);
        g.mostrarNodos(cmbOrigen);
        g.mostrarNodos(cmbDestino);
        
        g.mostrarAristas(tblAristas);
        
   
        // **Agrega manualmente el código para inicializar la tabla de grados**
        tblGrado = new javax.swing.JTable();
        jScrollPane4 = new javax.swing.JScrollPane();
        
        tblGrado.setModel(new javax.swing.table.DefaultTableModel(
            new Object[][]{},
            new String[]{"Vértice", "Grado"}
        ));
        jScrollPane4.setViewportView(tblGrado);
        
        tp.addTab("Grado de Nodos", jScrollPane4);
            
        // **Fin del código agregado manualmente**

        // Asegúrate de haber inicializado tblGrado antes de usarlo
        tblGrado.setModel(g.calcularGradoDeNodos());
         
        
        // Verificar si el grafo es conexo
        if (g.esConexo()) {
            System.out.println("El grafo es conexo");
            JOptionPane.showMessageDialog(this, "El grafo es conexo", "Resultado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            System.out.println("El grafo no es conexo");
            JOptionPane.showMessageDialog(this, "El grafo no es conexo", "Resultado", JOptionPane.WARNING_MESSAGE);
        }

    }

